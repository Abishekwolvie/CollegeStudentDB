package package1;

public class ECA {
	
	String name;
	String sports;
	String clubs;
	public ECA(String name, String sports, String clubs) {
		super();
		this.name = name;
		this.sports = sports;
		this.clubs = clubs;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSports() {
		return sports;
	}
	public void setSports(String sports) {
		this.sports = sports;
	}
	public String getClubs() {
		return clubs;
	}
	public void setClubs(String clubs) {
		this.clubs = clubs;
	}


}
